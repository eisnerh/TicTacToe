    //Cargando
    private class Loading implements ActionListener{

        @Override
        public void actionPerformed(ActionEvent e) {
            progreso=loading.getValue();
            if (progreso < 100){
                progreso++;
                loading.setValue(progreso);
            }else{
                tiempo.stop();
                Home.hide();
                inicioJuego.show();
            }
        }
    
}
    //asignar valores a los icones de los botones
    private void setIconXO (JButton btn){
        if (xo == 0 ){
            btn.setIcon(x);
            xo = 1;
            cont++;
            lblXO.setIcon(o);
            btn.setEnabled(false);
            lblJugador.setText(txtJugador2.getText());
        }else{
            btn.setIcon(o);
            xo = 0;
            cont++;
            lblXO.setIcon(x);
            btn.setEnabled(false);
            lblJugador.setText(txtJugador1.getText());
        }
    }
