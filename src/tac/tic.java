    //Presionar Casillas...
    private void CasillasClick (JButton btn, JButton a1, JButton a2, 
            JButton b1, JButton b2, JButton c1, JButton c2){
        //setIconXO(btn);
        if (btn.getIcon().equals(a1.getIcon())&&btn.getIcon().equals(a2.getIcon()) ||
                btn.getIcon().equals(b1.getIcon())&&btn.getIcon().equals(b2.getIcon())||
                btn.getIcon().equals(c1.getIcon())&&btn.getIcon().equals(c2.getIcon()))
        {
            if (btn.getIcon().equals(x)){
                Ganador.setText(txtJugador1.getText()+" Con X");
            }else{
                Ganador.setText(txtJugador1.getText()+" Con O");
            }
            finJuego();
        }
        Empate();
    }
    //Empate
    private void Empate(){
        if (cont ==9){
            finJuego();
            Ganador.setText("Empate");
        }
    }

